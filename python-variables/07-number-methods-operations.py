import math

print('*-----Arithmetic operations-----*')
print(23 + 12)
print(50 - 12)
print(23 * 2)
print(23 / 12)
print(23 // 4)
print(23 % 5)
print(23 ** 2)

print('*-----Augmented Assignment Operator-----*')
number = 10
print(number)
# number = number + 5
number += 5
print(number)
number *= 2
print(number)
number /= 5
print(number)

print('*-----Round-----*')
print(round(2.5))
print(round(213.542353))

print('*-----Absolute-----*')
print(abs(-23))
print(abs(+1.34))

print('*-----Math Library-----*')
print(math.sin(math.pi / 2))
print(math.cos(0))
print(math.ceil(2.1))
print(math.floor(2.9))
